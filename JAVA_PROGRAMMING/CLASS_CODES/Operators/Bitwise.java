

// Bitwise operators ( & , | , ^ , << , >> , >>>)
//
//
class Operators{

	public static void main(String[] args){
	
		int x = 5; 
		int y = 7;

		// bitwise ( & )
		System.out.println(x & y);// 5

		// Bitwise ( | )
		System.out.println(x | y);// 7
					  //
		// Bitwise ( << )
		System.out.println(x << 2);// it shifts the no of position to left side ( left shift)

		// Bitwise ( >> )
		System.out.println(y >> 2);// it shift the no of bits to right side ( right shift)
	}
}
