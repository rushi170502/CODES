
// WAP to find which element is greater 
// assume numbers are also same
//

class Demo{

	public static void main(String[] args){

		int a = 10;
		int b = 11;

		if( a == b)
			System.out.println(a+ " & " +b+ " are same");
		else if(a > b)
			System.out.println(a+ " is greater ");
		else
			System.out.println(b+ " is greater");
	}

}
